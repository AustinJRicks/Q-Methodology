<h2>About PaveTheGame, FOI-project TRV2013-16188</h2>
<h3>Overall objectives of the project</h3>
<ol>
<li>Create a simulated environment in which the interaction of the 
different roles in the procurement and construction of a road can be 
tested and improved.</li>
<li>Improve the knowledge of the working of the supply network for road construction in Sweden, including the supplying industries.</li>
<li>Advice to Trafikverket on the options for change in the procurement process.</li>
</ol>

